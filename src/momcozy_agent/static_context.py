def build_static_agent_context() -> str:
    lines = [
        "static_agent_context:",
        "service_selection: model_selects_service_skill_from_manifest",
        "",
        "global_safety_policy:",
        GLOBAL_SAFETY_POLICY,
        "",
        _section("skill_manifest", list_skill_manifests()),
    ]
    return "\n".join(line for line in lines if line)


def _section(name: str, value: Any) -> str:
    rendered = json.dumps(value, ensure_ascii=False, indent=2)
    return f"{name}:\n{rendered}"


STATIC_AGENT_INSTRUCTIONS = f"{BASE_AGENT_INSTRUCTIONS}\n\n{build_static_agent_context()}"
